from typing import Any

from pydantic import (
    BaseModel,
    ConfigDict,
    Field,
    StrictBool,
    StrictStr,
    field_validator,
)

from app.schemas.page_content import PageContentWorkspaceResponse


MIN_ORDER_VALUE = -2147483648
MAX_ORDER_VALUE = 2147483647

# Rust-compatible regex patterns.
# No lookahead / lookbehind.
CLEAN_TEXT_PATTERN = r"^[^\x00-\x20\x7F][^\x00-\x1F\x7F]*$"
CLEAN_DOCUMENT_ID_PATTERN = r"^[^\x00-\x20\x7F]+$"


# ---------------------------------------------------------
# Base config helpers
# ---------------------------------------------------------

class StrictRequestModel(BaseModel):
    """
    Base model for request bodies.

    extra="forbid" rejects unknown/random fields generated by fuzzing.
    """
    model_config = ConfigDict(extra="forbid")


# ---------------------------------------------------------
# Reusable validation helpers
# ---------------------------------------------------------

def validate_order_not_bool(value: Any) -> Any:
    """
    Reject booleans for order.

    Important:
    - OpenAPI should still expose order as normal integer with min/max.
    - Pydantic should still reject true/false.
    """
    if isinstance(value, bool):
        raise ValueError("Order must be an integer, not a boolean")

    return value


# ---------------------------------------------------------
# Access control schemas
# ---------------------------------------------------------

class AccessRoleResponse(BaseModel):
    name: StrictStr
    scope: StrictStr
    program: StrictStr = ""
    function: StrictStr = ""


class AccessUserResponse(BaseModel):
    email: StrictStr


class AccessGroupResponse(BaseModel):
    roles: list[AccessRoleResponse] = Field(default_factory=list)
    users: list[AccessUserResponse] = Field(default_factory=list)


class AccessControlResponse(BaseModel):
    admins: AccessGroupResponse = Field(default_factory=AccessGroupResponse)
    viewers: AccessGroupResponse = Field(default_factory=AccessGroupResponse)


# ---------------------------------------------------------
# Tab response schemas
# ---------------------------------------------------------

class TabParentResponse(BaseModel):
    id: int | None = None
    documentId: StrictStr | None = None
    title: StrictStr | None = None
    order: int = 0


class TabSummaryResponse(BaseModel):
    id: int | None = None
    documentId: StrictStr | None = None
    title: StrictStr | None = None
    order: int = 0

    locked: StrictBool = False
    locked_by: StrictStr = ""

    has_children: StrictBool = False
    has_content: StrictBool = False


class TabWorkspaceResponse(BaseModel):
    id: int | None = None
    documentId: StrictStr | None = None
    title: StrictStr | None = None
    order: int = 0

    parent: TabParentResponse | None = None
    page_content: PageContentWorkspaceResponse | None = None

    # Allows normalized DB rows that may return a plain dict.
    access_control: AccessControlResponse | dict[str, Any] = Field(
        default_factory=AccessControlResponse
    )

    locked: StrictBool = False
    locked_by: StrictStr = ""

    children: list[TabSummaryResponse] = Field(default_factory=list)


class BreadcrumbItemResponse(BaseModel):
    id: int | None = None
    documentId: StrictStr | None = None
    title: StrictStr | None = None
    order: int = 0


# ---------------------------------------------------------
# Standard API wrapper responses
# ---------------------------------------------------------

class TabSummaryListAPIResponse(BaseModel):
    data: list[TabSummaryResponse] = Field(default_factory=list)


class TabWorkspaceAPIResponse(BaseModel):
    data: TabWorkspaceResponse


class BreadcrumbAPIResponse(BaseModel):
    data: list[BreadcrumbItemResponse] = Field(default_factory=list)


# ---------------------------------------------------------
# Request schemas
# ---------------------------------------------------------

class CreateTabRequest(StrictRequestModel):
    title: StrictStr = Field(
        min_length=1,
        max_length=255,
        pattern=CLEAN_TEXT_PATTERN,
    )

    parentDocumentId: StrictStr | None = Field(
        default=None,
        min_length=1,
        max_length=255,
        pattern=CLEAN_DOCUMENT_ID_PATTERN,
    )

    content: dict[str, Any] | list[Any] | None = None

    order: int | None = Field(
        default=None,
        ge=MIN_ORDER_VALUE,
        le=MAX_ORDER_VALUE,
    )

    # IMPORTANT:
    # The frontend sends inherited access_control when creating a tab.
    # Without this field, FastAPI rejects the request with 422.
    access_control: AccessControlResponse | dict[str, Any] | None = None

    @field_validator("order", mode="before")
    @classmethod
    def validate_order(cls, value: Any) -> Any:
        return validate_order_not_bool(value)


class UpdateTabRequest(StrictRequestModel):
    title: StrictStr | None = Field(
        default=None,
        min_length=1,
        max_length=255,
        pattern=CLEAN_TEXT_PATTERN,
    )

    order: int | None = Field(
        default=None,
        ge=MIN_ORDER_VALUE,
        le=MAX_ORDER_VALUE,
    )

    access_control: AccessControlResponse | dict[str, Any] | None = None

    locked: StrictBool | None = None

    locked_by: StrictStr | None = Field(
        default=None,
        max_length=255,
        pattern=CLEAN_TEXT_PATTERN,
    )

    @field_validator("order", mode="before")
    @classmethod
    def validate_order(cls, value: Any) -> Any:
        return validate_order_not_bool(value)


class MoveTabRequest(StrictRequestModel):
    newParentDocumentId: StrictStr | None = Field(
        default=None,
        min_length=1,
        max_length=255,
        pattern=CLEAN_DOCUMENT_ID_PATTERN,
    )

    order: int | None = Field(
        default=None,
        ge=MIN_ORDER_VALUE,
        le=MAX_ORDER_VALUE,
    )

    @field_validator("order", mode="before")
    @classmethod
    def validate_order(cls, value: Any) -> Any:
        return validate_order_not_bool(value)


class ReorderTabItemRequest(StrictRequestModel):
    documentId: StrictStr = Field(
        min_length=1,
        max_length=255,
        pattern=CLEAN_DOCUMENT_ID_PATTERN,
    )

    order: int = Field(
        ge=MIN_ORDER_VALUE,
        le=MAX_ORDER_VALUE,
    )

    @field_validator("order", mode="before")
    @classmethod
    def validate_order(cls, value: Any) -> Any:
        return validate_order_not_bool(value)


class ReorderTabsRequest(StrictRequestModel):
    items: list[ReorderTabItemRequest] = Field(min_length=1)


class UpdateTabContentRequest(StrictRequestModel):
    content: dict[str, Any] | list[Any] | None = None


class LockTabRequest(StrictRequestModel):
    locked_by: StrictStr = Field(
        min_length=1,
        max_length=255,
        pattern=CLEAN_TEXT_PATTERN,
    )


class UnlockTabRequest(StrictRequestModel):
    unlocked_by: StrictStr | None = Field(
        default=None,
        max_length=255,
        pattern=CLEAN_TEXT_PATTERN,
    )

    force: StrictBool = False